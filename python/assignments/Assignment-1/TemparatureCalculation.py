
# The Temperature of a city in Fahrenheit degrees is input through the keyboard. Write a program to
# convert this temperature into Centigrade degrees.

#taking input of the temp of any city in farenheit degree

degree_f=float(input("enter temp in farenheit degree :"))


# calculating temp in celcius

degree_c= (degree_f-32)*9/5


# printing output
print(f"{degree_f} degree farenheight =  {degree_c} centegrade degree")


